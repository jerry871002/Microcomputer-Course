	ORG 00H
	AJMP LED_SHOW
	ORG 50H
;=====LED SHOW========================== 
LED_SHOW:
	MOV R0, #72
	MOV DPTR, #TABLE 
	MOV R1, #24
START:
	JNB P3.2, NEXT_COLUMN
	AJMP START
NEXT_COLUMN:
	CALL READ_BYTE
	MOV P0, A
	CALL READ_BYTE
	MOV P2, A
	CALL READ_BYTE
	MOV P1, A
	CALL DELAY
	DJNZ R1, NEXT_COLUMN
	AJMP LED_SHOW
;=====READ 1 BYTE FROM TABLE============
READ_BYTE:
	DEC R0
	MOV A, R0
	MOVC A, @A+DPTR 
	CPL A
	RET

DELAY:
	MOV R5,#20H
DELAY1:
	MOV R6,#25H
DELAY2:
	MOV R7,#05H
DELAY3:
	DJNZ R7,DELAY3
	DJNZ R6,DELAY2
	DJNZ R5,DELAY1
	RET

