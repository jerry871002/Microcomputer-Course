	ORG 0000H
	JMP START
	ORG 0050H
START:
	MOV A, #00010001B
	MOV R0, #0
RIGHT:
	MOV P0, A
	CALL DELAY
	RR A
	INC R0
	CJNE R0, #128, RIGHT
	MOV R0, #0
LEFT:
	MOV P0, A
	CALL DELAY
	RL A	
	INC R0
	CJNE R0, #64, LEFT
	MOV R0, #0
	AJMP RIGHT
DELAY:
	MOV R6, #230
DELAY1:
	MOV R7, #255
DELAY2:
	DJNZ R7,DELAY2
	DJNZ R6,DELAY1
	RET
END