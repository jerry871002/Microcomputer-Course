	ORG 00H
	JMP CHOOSE
	ORG 50H
CHOOSE:
	MOV R2, #0		;選字
START:
	MOV R0, #0		;R0: TABLE index
	MOV R1, #10H	;R1 initial 00010000B

	CJNE R2, #0, NOTBIG
	MOV DPTR, #TABLEBIG
	JMP LOOP
NOTBIG:
	CJNE R2, #1, NOTMEDIUM
	MOV DPTR, #TABLEMEDIUM
	JMP LOOP
NOTMEDIUM:
	MOV DPTR, #TABLESMALL
LOOP:
	MOV R3, #0FFH
LOOPMAIN:			;P1控制5那邊，P0控制7那邊
	MOV P1, #00H	;關閉LED
	MOV A, R0		;R0: TABLE index
	MOVC A, @A+DPTR
	MOV P0, A
	MOV P1, R1
	INC R0

	MOV A, R1
	RR A
	MOV R1, A

	CALL DELAY
	CJNE R0, #5, LOOPMAIN
	MOV R0, #0		;R0: TABLE index
	MOV R1, #10H	;R1 initial 00010000B
	DJNZ R3, LOOPMAIN
	INC R2
	CJNE R2, #3, START
	MOV R2, #00H
	JMP START
TABLEBIG:
	DB 12H			;0 0 0 1 0 0 1 0
	DB 14H			;0 0 0 1 0 1 0 0
	DB 78H			;0 1 1 1 1 0 0 0
	DB 14H			;0 0 0 1 0 1 0 0
	DB 12H			;0 0 0 1 0 0 1 0
TABLEMEDIUM:
	DB 3CH			;0 0 1 1 1 1 0 0
	DB 24H			;0 0 1 0 0 1 0 0
	DB 7FH			;0 1 1 1 1 1 1 1
	DB 24H			;0 0 1 0 0 1 0 0
	DB 3CH			;0 0 1 1 1 1 0 0
TABLESMALL:
	DB 08H			;0 0 0 0 1 0 0 0
	DB 14H			;0 0 0 1 0 1 0 0
	DB 7EH			;0 1 1 1 1 1 1 0
	DB 10H			;0 0 0 1 0 0 0 0
	DB 08H			;0 0 0 0 1 0 0 0

DELAY:
	MOV R6, #30H
DELAY1:
	MOV R7, #30H
DELAY2:
	DJNZ R7, DELAY2
	DJNZ R6, DELAY1
	RET

