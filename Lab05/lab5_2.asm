	ORG 00H
	JMP MAIN
	ORG 50H
MAIN:
	MOV R0, #10H	;00010000B
	MOV R1, #40H	;01000000B

LOOP:
	MOV P1, R0
	MOV P0, R1

	MOV A, R1
	RR A
	MOV R1, A

	CALL DELAY

	CJNE R1, #80H, LOOP
	MOV A, R1
	RR A
	MOV R1, A
	MOV A, R0
	RR A
	MOV R0, A
	CJNE R0, #80H, LOOP

	JMP MAIN

DELAY:
	MOV R6, #0FFH
DELAY1:
	MOV R7, #0FFH
DELAY2:
	DJNZ R7, DELAY2
	DJNZ R6, DELAY1
	RET