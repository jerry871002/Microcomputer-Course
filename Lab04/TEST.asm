	ORG 0000H
	AJMP MAIN
	ORG 0050H
MAIN:
	MOV R2, #0
LOOP:
	MOV P2, #0FFH
	MOV A, R2
	INC A
	MOV DPTR, #NUMTABLE
	MOVC A, @A+DPTR
	MOV P1, A

	MOV A, R2
	MOV DPTR, #POS
	MOVC A, @A+DPTR
	MOV P2, A

	ACALL DELAY
	INC R2
	CJNE R2, #4, LOOP
	AJMP MAIN

DELAY:
	MOV R5, #0FFH
DELAY1:
	MOV R6, #01H
DELAY2:
	MOV R7, #01H
DELAY3:
	DJNZ R7, DELAY3
	DJNZ R6, DELAY2
	DJNZ R5, DELAY1
	RET
;===========================
NUMTABLE:
	DB 0C0H ;0
	DB 0F9H ;1
	DB 0A4H ;2
	DB 0B0H ;3
	DB 099H ;4
	DB 092H ;5
	DB 082H ;6
	DB 0F8H ;7
	DB 080H ;8
	DB 090H ;9
;===========================
POS:
	DB 0F7H, 0FBH, 0FDH, 0FEH