	ORG 0000H
	AJMP MAIN
	ORG 0050H
MAIN:
	MOV DPTR, #NUMTABLE
	CLR C
SETCOUNTER:
	MOV R0, #0AH ;計數器
LOOP: 
	DEC R0
	MOV A, #9H
	SUBB A, R0
	MOVC A, @A+DPTR
	MOV P1, A
	ACALL DELAY
	CJNE R0, #0H, LOOP
	AJMP SETCOUNTER
;===========================
DELAY:
	MOV R5, #0FFH
DELAY1:
	MOV R6, #0FFH
DELAY2:
	MOV R7, #30H
DELAY3:
	DJNZ R7, DELAY3
	DJNZ R6, DELAY2
	DJNZ R5, DELAY1
	RET
;============================
NUMTABLE:
	DB 0C0H ;0
	DB 0F9H ;1
	DB 0A4H ;2
	DB 0B0H ;3
	DB 099H ;4
	DB 092H ;5
	DB 082H ;6
	DB 0F8H ;7
	DB 080H ;8
	DB 090H ;9

