	ORG 0000H
	AJMP MAIN
	ORG 0050H
MAIN:
	MOV DPTR, #NUMTABLE
	CLR C
SETCOUNTER:
	MOV R0, #0EH ;計數器
LOOP: 
	DEC R0
	MOV A, #0DH
	SUBB A, R0
	MOVC A, @A+DPTR
	MOV P1, A
	ACALL DELAY
	CJNE R0, #0H, LOOP
	AJMP SETCOUNTER
;===========================
DELAY:
	MOV R5, #0FFH 
DELAY1:
	MOV A, P2
	ORL A, #1FH
	MOV R6, A
DELAY2:
	MOV R7, #35H 
DELAY3:
	DJNZ R7, DELAY3 
	DJNZ R6, DELAY2 
	DJNZ R5, DELAY1 
	RET
;============================
NUMTABLE:
	DB 0C0H ;0
	DB 0FFH ;暗
	DB 082H ;6
	DB 0FFH ;暗
	DB 0F9H ;1
	DB 0FFH ;暗
	DB 0C0H ;0
	DB 0FFH ;暗
	DB 0F8H ;7
	DB 0FFH ;暗
	DB 080H ;8
	DB 0FFH ;暗
	DB 0C0H ;0
	DB 0FFH ;暗

